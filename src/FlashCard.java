public class FlashCard {
    private String question;
    private String answer;

    public void setQuestion(String q) {
        this.question = q;
    }
    public void setAnswer(String a) {
        this.answer = a;
    }
    public String getQuestion() {
        return this.question;
    }
    public String getAnswer() {
        return this.answer;
    }
}
